/**
 * Модуль определения ударений в русских словах.
 *
 * Текущая реализация — эвристики. Полная версия будет использовать:
 * - Словарь ударений (OpenRussian.org / Zaliznyak)
 * - ML-модель (rustress / DeepStress)
 *
 * Эвристики покрывают ~60% случаев; для точного анализа ударений
 * рекомендуется ручная разметка (заглавные гласные) или подключение словаря.
 */

import { getVowelPositions } from './syllableCounter';

// ==================== Константы ====================

/** Гласные буквы */
const VOWELS = new Set('аеёиоуыэюя');

/** Прилагательные: окончания мужского рода — ударение обычно на окончание */
const ADJ_ENDINGS_M = ['ий', 'ый', 'ой', 'ей'];

// ==================== Внутренние утилиты ====================

/** Нижний регистр */
function toLower(word: string): string {
  return word.toLowerCase();
}

/** Количество гласных в слове */
function vowelCount(word: string): number {
  let count = 0;
  for (const ch of word) {
    if (VOWELS.has(ch)) count++;
  }
  return count;
}

/**
 * Находит позицию последней гласной в слове (по индексам символов).
 * @returns -1 если гласных нет
 */
function lastVowelPos(word: string): number {
  const lower = toLower(word);
  for (let i = lower.length - 1; i >= 0; i--) {
    if (VOWELS.has(lower[i])) return i;
  }
  return -1;
}

// ==================== Публичный API ====================

/**
 * Определяет позицию ударной гласной в слове.
 *
 * Возвращает индекс (0-based) ударной гласной в строке слова,
 * или -1 если не удалось определить.
 *
 * Правила (в порядке приоритета):
 * 1. Односложные слова → ударение на единственную гласную
 * 2. Заглавная гласная в слове → ударная (поддержка ручной разметки)
 * 3. Глаголы на -иться, -еться → ударение на гласную перед -ся
 * 4. Глаголы на -овать → ударение на «о» перед «вать»
 * 5. Прилагательные на -ий, -ый, -ой, -ей (полные) → ударение часто на окончание
 * 6. Краткие прилагательные на -ён, -ен → ударение на суффикс
 * 7. Слова из словаря частых исключений
 * 8. Двусложные существительные → склон к первому слогу
 * 9. Двусложные глаголы → склон ко второму слогу
 * 10. Иначе → -1 (неизвестно)
 *
 * @param word — чистое слово без пунктуации
 */
export function detectStress(word: string): number {
  if (!word) return -1;

  // Убираем не-русские символы на краях (но не дефисы)
  const clean = word.replace(/^[^а-яА-ЯёЁ]+/, '').replace(/[^а-яА-ЯёЁ]+$/, '');
  if (!clean) return -1;

  const lower = clean.toLowerCase();
  const vcount = vowelCount(lower);

  // Односложное слово
  if (vcount === 1) {
    return lastVowelPos(clean);
  }

  // Проверяем ручную разметку (заглавная гласная)
  for (let i = 0; i < clean.length; i++) {
    const ch = clean[i];
    if (VOWELS.has(toLower(ch)) && ch !== toLower(ch)) {
      return i;
    }
  }

  // Глаголы на -иться / -еться → ударение на гласную перед -ся
  if (lower.endsWith('ться') || lower.endsWith('тся')) {
    // Убираем «ться» / «тся», ищем гласную перед «ся»
    const withoutReflexive = lower.slice(0, -4); // убираем «ться»
    if (withoutReflexive.length > 0) {
      const pos = lastVowelPos(withoutReflexive);
      if (pos !== -1) return pos;
    }
  }

  // Глаголы на -овать → ударение на «о»
  if (lower.endsWith('овать')) {
    const oPos = lower.lastIndexOf('о');
    if (oPos !== -1) return oPos;
  }

  // Краткие прилагательные на -ён / -ен → ударение на «ё»/«е» суффикса
  if (lower.endsWith('ён') || lower.endsWith('ен')) {
    const stressVowel = lower.endsWith('ён')
      ? lower.lastIndexOf('ё')
      : lower.lastIndexOf('е');
    if (stressVowel !== -1) return stressVowel;
  }

  // Словарь частых слов (ограниченный набор для основных случаев)
  const dictPos = lookupDictionary(lower);
  if (dictPos !== -1) return dictPos;

  // Прилагательные на -ий, -ый, -ой, -ей → склон к ударению на окончание
  if (ADJ_ENDINGS_M.some(e => lower.endsWith(e))) {
    // Для прилагательных ударение часто на окончание
    const pos = lastVowelPos(lower);
    if (pos !== -1) return pos;
  }

  // Двусложные слова: существительные → первый слог, глаголы → второй
  if (vcount === 2) {
    const positions = getVowelPositions(lower);
    if (positions.length === 2) {
      // Эвристика: если слово заканчивается на согласную — скорее существительное
      const lastChar = lower[lower.length - 1];
      if (!VOWELS.has(lastChar)) {
        // Скорее существительное → ударение на первый слог
        return positions[0];
      }
      // Если ends with vowel — может быть глагол → второй слог
      return positions[1];
    }
  }

  // Для более длинных слов — пока не определяем
  return -1;
}

/**
 * Определяет ударения во всех словах строки.
 *
 * @param line — строка текста
 * @returns Map: слово (lowercase) → позиция ударной гласной
 */
export function detectStressInLine(line: string): Map<string, number> {
  const result = new Map<string, number>();

  // Разбиваем на слова по whitespace
  const tokens = line.trim().split(/\s+/);
  for (const token of tokens) {
    // Убираем пунктуацию по краям
    const clean = token.replace(/^[^а-яА-ЯёЁ-]+/, '').replace(/[^а-яА-ЯёЁ-]+$/, '');
    if (!clean) continue;

    const pos = detectStress(clean);
    if (pos !== -1) {
      result.set(clean.toLowerCase(), pos);
    }
  }

  return result;
}

