/**
 * Ищет ударение во внутреннем словаре.
 * @returns позиция ударной гласной или -1
 */
function lookupDictionary(lowerWord: string): number {
  const pos = STRESS_DICTIONARY.get(lowerWord);
  return pos !== undefined ? pos : -1;
}
